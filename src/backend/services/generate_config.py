from aerleon.lib import naming
from aerleon import api as aerleon_api

from backend.objects.attributes.address import Address
from backend.objects.attributes.address_group import AddressGroup
from backend.objects.attributes.service import Service
from backend.objects.attributes.service_group import ServiceGroup


class PolicyRule:
    def __init__(
        self,
        name: str,
        obj_type: str,
        action: str,
        object: Address | Service | AddressGroup | ServiceGroup,
    ):
        self.name = name
        self.type = obj_type
        self.action = action
        self.object = object
        self.members = []
        if obj_type.lower() == "address_group" and isinstance(object, AddressGroup):
            self.members = object.get_addresses()
        elif obj_type.lower() == "service_group" and isinstance(object, ServiceGroup):
            self.members = object.get_services()


class Policy:
    """
    A class for the input to aerleons Generate function.
    This class will take in a list of rules and convert them into the format that aerleon expects.
    The rules will be in the form of our internal data models (Address, Service, AddressGroup, ServiceGroup)
    and this class will convert them into the format that aerleon expects.

    Args:
        name (str): The name of the policy.
        rules (list[Object]): A list of rules in the form of our internal data models (Address, Service, AddressGroup, ServiceGroup).
        vendor (str): The name of the vendor for which to generate the configuration.
        type (str): The type of policy to generate (e.g. "acl", "nat", etc.).
    """

    def __init__(self, name: str, rules: list, vendor: str, policy_type: str):
        self.name = name
        self.YAMLConfig = {
            "filename": name,
            "filters": {
                "header": {
                    "targets": {vendor: name},
                    "comment": f"Generated by Rulio for {vendor} {policy_type} policy",
                },
                "terms": [],
            },
        }

        self.networks = {"networks": {}}
        for rule in rules:
            match rule.type:
                case "address":
                    self.add_address(rule)
                case "service":
                    self.add_service(rule)
                case "address_group":
                    self.add_address_group(rule)
                case "service_group":
                    self.add_service_group(rule)
                case _:
                    raise ValueError(
                        f"Unsupported rule type: {rule.__class__.__name__}"
                    )

    def add_address(self, rule: PolicyRule):
        self.YAMLConfig["terms"].append(
            {
                "name": rule.name,
                "destination-address": rule.ip_address,
                "action": rule.action,
            }
        )

    def add_service(self, rule: PolicyRule):
        self.YAMLConfig["terms"].append(
            {
                "name": rule.name,
                "service": rule.service,
                "action": rule.action,
            }
        )

    def add_address_group(self, rule: PolicyRule):
        ip_addresses = rule.members
        self.networks["networks"][rule.name]["values"] = ip_addresses

    def add_service_group(self, rule: PolicyRule):
        pass


def generate_config(policy: Policy, vendor: str) -> str:
    """
    Generates a configuration for the specified vendor based on the provided YAML configuration.

    Args:
        policy (Policy): The policy object containing the rules and configuration to be converted.
        vendor (str): The name of the vendor for which to generate the configuration.

    Returns:
        str: The generated configuration as a string.
    """
    definitions = naming.Naming()
    definitions.ParseDefinitionsObject(policy.networks)
    configs = aerleon_api.Generate([policy.YAMLConfig], definitions)
    return configs
